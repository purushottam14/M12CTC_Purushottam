package com.purushottam.DataTypes;

public class NumericInteger {

		public static void main(String[] args) {
	 
			int max = 2147483647;
			int min = -2147483648;
			short shortMax = 32767;
			short shortMin = -32768;
			long longMax = 9223372036854775807L;
			long longMin = -9223372036854775808L;
			byte byteMax = 127;
			byte byteMin = -128;
			System.out.println("Integer max :"+max);
			System.out.println("Integer min :"+min);
			System.out.println("Short max :"+shortMax);
			System.out.println("Short max :"+shortMin);
			System.out.println("Long max :"+longMax);
			System.out.println("Long max :"+longMin);
			System.out.println("Byte max :"+byteMax);
			System.out.println("Byte max :"+byteMin);
		}
	}



